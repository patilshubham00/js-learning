console.log("========= Compound Addition += operator ==============");
var n1 = 10;
n1 += 5;// n1 = n1 + 5 
console.log(`Compound addition Result is: ${n1}`);




console.log("========= Compound Subtraction -= operator ==============");
var n1 = 10;
n1 -= 5;// n1 = n1 - 5 
console.log(`Compound Subtraction Result is: ${n1}`);




console.log("========= Compound Multiplication *= operator ==============");
var n1 = 10;
n1 *= 5;// n1 = n1 * 5 
console.log(`Compound Multiplication Result is: ${n1}`);



console.log("========= Compound Division /= operator ==============");
var n1 = 10;
n1 /= 5;// n1 = n1 - 5 
console.log(`Compound Division Result is: ${n1}`);




console.log("========= Compound Modulus %= operator ==============");
var n1 = 10;
n1 %= 5;// n1 = n1 * 5 
console.log(`Compound Modulus Result is: ${n1}`);
